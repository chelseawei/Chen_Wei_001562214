    public void populateTable(){
        DefaultTableModel model1 = (DefaultTableModel)table1.getModel();
        model1.setRowCount(0);
        for(Customer c : system.getCustomerDirectory().getCustomerList()){
            if(c.getUsername().equals(userAccount.getUsername())){
                for(Order order : c.getOrders()){
                    if(order.getStatus().equals("Completed")){
                    Object[] row = new Object[6];
                    row[0] = order;
                    row[1] = order.getRestaurant();
                    row[2] = order.getMenu();
                    row[3] = order.getDeliveryman();
                    row[4] = order.getStatus();
                    row[5] = order.getComment();
           
                    model1.addRow(row);    
                    }
                }
            }
            
        }
        
        
    }

