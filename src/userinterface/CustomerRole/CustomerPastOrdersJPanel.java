    private void btnSubmmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmmitActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model1 = (DefaultTableModel)table1.getModel();
        int row = table1.getSelectedRow();
        // System.out.println(row);
        if(row < 0) {
            JOptionPane.showMessageDialog(this, "please select the row to comment");
            return;
        }else{
             Order order = (Order) model1.getValueAt(row, 0);
             order.setComment(txtComment.getText());
        }
        populateTable();
    }//GEN-LAST:event_btnSubmmitActionPerformed

